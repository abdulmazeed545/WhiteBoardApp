/// Enum representing different types of shapes that can be drawn.
enum ShapeType {
  line,        // Straight line
  rectangle,   // Rectangle shape
  ellipse,     // Circle/ellipse shape
  triangle,    // Triangle shape
  arrow,       // Single-headed arrow
  doubleArrow, // Double-headed arrow
  text,        // Text element
}

/// Enum representing text alignment options.
enum TextAlignment {
  left,   // Left-aligned text
  center, // Center-aligned text
  right,  // Right-aligned text
} 